class Command(BaseCommand):
    help = "Заповнити чергу тем для блогу (100 тем, найсильніші першими)"

    def add_arguments(self, parser):
        parser.add_argument(
            "--reset-ranks",
            action="store_true",
            help="Переставити ранги наявних тем за цим списком.",
        )

    def handle(self, *args, **options):
        rows = TIER_1 + TIER_2 + TIER_3 + TIER_4
        created = updated = 0

        for index, (title, brief, target) in enumerate(rows, start=1):
            topic, was_created = ArticleTopic.objects.get_or_create(
                title=title,
                defaults={"brief": brief, "target_path": target, "rank": index},
            )
            if was_created:
                created += 1
                continue

            # A topic already turned into an article keeps its place in
            # history; re-seeding must not put it back in the queue.
            if topic.status != ArticleTopic.Status.PENDING:
                continue

            topic.brief = brief
            topic.target_path = target
            if options["reset_ranks"]:
                topic.rank = index
            topic.save()
            updated += 1

        total = ArticleTopic.objects.count()
        pending = ArticleTopic.objects.filter(
            status=ArticleTopic.Status.PENDING
        ).count()
        self.stdout.write(
            f"Тем у списку: {len(rows)} · створено {created} · оновлено {updated}"
        )
        self.stdout.write(f"У базі: {total}, з них у черзі: {pending}")
        self.stdout.write(
            f"Вистачить на {pending // 52} р. {pending % 52} тиж. щотижневих постів"
        )
